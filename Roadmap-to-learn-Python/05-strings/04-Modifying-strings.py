# Using string methods

text = " Hello, World!"
txt = "arjun"

print(text.strip())  # removes leading and trailing whitespace

print(text.lower())  # converts to lowercase

print(text.upper())  # converts to uppercase

print(text.replace("World", "Python"))  # replaces "World" with "Python"

print(txt.title())  # converts the first character of each word to uppercase

print(txt.split(" "))  # splits the string into a list where each word is a list item 




