# List items are indexed and you can access them by referring to the index number

mylist = ["apple", "banana", "cherry","Orange"]

print(mylist[0])  # Accessing the first element

print(mylist[1])  # Accessing the second element

print(mylist[-1])  # Accessing the last element using negative indexing

print(mylist[0:2])  # Returns the first two elements

print(mylist[:3]) # Returns the first three elements

print(mylist[1:])  # Returns all elements from the second element onwards

