# Loop Lists
# Looping through a list using a for loop
mylist = ["apple", "banana", "cherry", "Orange"]
for x in mylist:
    print(x)  # Output: apple, banana, cherry, Orange (each on a new line)

# Looping through a list using a while loop
index = 0
while index < len(mylist):
    print(mylist[index])  # Output: apple, banana, cherry, Orange (each on a new line)
    index += 1