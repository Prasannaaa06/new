mylist = ["apple", "banana", "cherry","Orange"]

print(mylist)  # Printing the entire list

print(mylist[0])  # Accessing the first element

print(mylist[1])  # Accessing the second element

print(mylist[-1])  # Accessing the last element using negative indexing

print(mylist[0:2])  # Slicing the list to get the first two elements

print(mylist[1:])  # Slicing the list to get all elements from the second element onwards

print(mylist[:2])  # Slicing the list to get the first two elements

print(mylist[1:2])  # Slicing the list to get the second element only

print(mylist[1:3])  # Slicing the list to get the second and third elements

print(mylist[0:3])  # Slicing the list to get the first three elements

print(mylist[0:])  # Slicing the list to get all elements from the first element onwards

print(mylist[:])  # Slicing the list to get all elements

print(mylist[-3:-1])  # Slicing the list to get the first two elements using negative indexing

print(mylist[-2:])  # Slicing the list to get all elements from the second last element onwards

print(mylist[:-2])  # Slicing the list to get all elements except the last two

print(mylist[-2:-1])  # Slicing the list to get the second last element only

print(mylist[-3:-2])  # Slicing the list to get the third last element only

print(mylist[-3:])  # Slicing the list to get all elements from the third last element onwards

print(mylist[-1:])  # Slicing the list to get the last element only

print(mylist[0:3:2])  # Slicing the list to get every second element from the first three elements

print(mylist[::2])  # Slicing the list to get every second element from the entire list

print(mylist[1::2])  # Slicing the list to get every second element starting from the second element

print(mylist[::-1])  # Reversing the list using slicing

print(mylist[::-2])  # Reversing the list and getting every second element

print(mylist[1::-1])  # Slicing the list to get the first two elements in reverse order

print(mylist[1:2:-1])  # Slicing the list to get the second element in reverse order (will return an empty list)

print(mylist[1:3:-1])  # Slicing the list to get the second and third elements in reverse order (will return an empty list)

print(mylist[0:3:-1])  # Slicing the list to get the first three elements in reverse order (will return an empty list)

print(mylist[0::-1])  # Slicing the list to get the first element in reverse order (will return an empty list)

